let diaSemana = 5;
switch (diaSemana) {
  case 1:
    console.log("Lunes");
    break;
  case 2:
    console.log("martes");
    break;
  case 3:
    console.log("miercoles");
    break;
  case 4:
    console.log("jueves");
    break;
  case 5:
    console.log("viernes");
    break;
  case 6:
    console.log("Sabado");
    break;
  case 7:
    console.log("domingo");
    break;
    default:
        console.log("Dia equivocado tienes que introducir del uno al 7");
}
